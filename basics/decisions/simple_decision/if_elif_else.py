print("Towards which direction should I paint(up,down,left or right)?")
direction=input()

if(direction=="up"):
  print("\n I am painting in the upward direction!")
elif(direction=="down"):
  print("\n I am painting in the downward direction!")
elif(direction=="left"):
  print("\n I am painting in the leftward direction! ")
elif(direction=="right"):
  print("\n I am painting in the rightward direction!")
else:
  print("\nNot sure which direction I am painting in!")
  
  